    // cek submit form address
    function ceksubmitdocument(w)
    {
        var form = w,
        modal = form.parents('.new-modal.show'),
        button = modal.find('button.modal-submit'),
        card = $('body').find('.cmd-modal[data-target="#'+modal.attr('id')+'"]').parents('.card-body');

        var type = form.find("*[name='document_type']"),
        label = form.find("*[name='document_label']"),
        expired = form.find("*[name='expired_date']"),
        duration = form.find("*[name='duration']"),
        file = form.find("*[name='file']");


        if( button.attr('role') === 'off')
        {
            button.attr('role', 'on');
            form.find(".br-error").removeClass("br-error");
            form.find("span.error").remove();

            if( $.trim(type.val()) === '' || $.trim(label.val()).length < 3 || $.trim(expired.val()) === '' || $.trim(duration.val()) === '' || $.trim(file.val()) === '' )
            {
                if( $.trim(type.val()) === '')
                {
                    type.parent(".ar-content").find("button.btn").addClass("br-error");
                    type.parent(".ar-content").append("<span class='error'>Harap pilih jenis dokumen</span>");
                }

                if( $.trim(label.val()).length < 3 )
                {
                    label.parent(".ar-content").append("<span class='error'>Nama atau label dokumen sekurang nya 3 karakter</span>");
                    label.addClass("br-error");
                }

                if( $.trim(expired.val()) === '')
                {
                    expired.parents(".ar-content").append("<span class='error'>Tentukan expired date</span>");
                    expired.addClass("br-error");
                }

                if( $.trim(file.val()) === '')
                {
                    file.parent(".ar-content").append("<span class='error'>Lampirkan file jenis .pdf</span>");
                    file.parent(".ar-content").find("a").addClass("br-error");
                }

                button.attr("role", "off");
                return false;
            }
            return true;
        } 


    }

