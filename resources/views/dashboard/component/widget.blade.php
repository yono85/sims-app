        var textx = parseFloat(text.val());
        if( textx > max)
        {
            text.parents('.area-quantity').find('.error-qty').html('Maks. beli ' + text.attr('max') + ' qty');
        }
        else
        {
            text.parents('.area-quantity').find('.error-qty').html('');
            cart_updateqtywg(area);
        }


        if( parseFloat( text.val() ) > 1)
        {
            area.find('button.cmd-updateqtycart[data-type="min"]').removeAttr('disabled');
        }
        else
        {
            area.find('button.cmd-updateqtycart[data-type="min"]').attr('disabled','disabled');
        }

        if( parseFloat(text.val()) >= parseFloat(max) )
        {
            area.find('button.cmd-updateqtycart[data-type="add"]').attr('disabled','disabled');   
        }
        else
        {
            area.find('button.cmd-updateqtycart[data-type="add"]').removeAttr('disabled'); 
